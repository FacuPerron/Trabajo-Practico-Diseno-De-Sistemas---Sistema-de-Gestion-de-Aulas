    private void IniciarSesionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_IniciarSesionActionPerformed
        try{
            iniciarSesion(IdUsuario.getText());
        }
        catch (CamposObligatoriosVaciosException ex){
            JOptionPane.showMessageDialog(null, "Error: hay un campo vacío", "Sesion", JOptionPane.ERROR_MESSAGE);
        }
        catch (OpcionNoSeleccionadaException ex){
            JOptionPane.showMessageDialog(null, "Error: por favor elija si es Administrador o Bedel", "Sesion", JOptionPane.ERROR_MESSAGE);
        }
        catch (UsuarioInexistenteException | ContraseñaEquivocadaException ex){
            JOptionPane.showMessageDialog(null, "Error: usuario o contraseña invalida", "Sesion", JOptionPane.ERROR_MESSAGE);
        } catch (NoSuchAlgorithmException ex) {
            Logger.getLogger(InicioSesion.class.getName()).log(Level.SEVERE, null, ex);
        } catch (InvalidKeySpecException ex) {
            Logger.getLogger(InicioSesion.class.getName()).log(Level.SEVERE, null, ex);
        } catch (LongitudesMaximasException ex) {
            JOptionPane.showMessageDialog(null, "Error: longitud maxima de algun campo excedida", "Sesion", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_IniciarSesionActionPerformed
    private void iniciarSesion(String usuario) throws UsuarioInexistenteException, CamposObligatoriosVaciosException, ContraseñaEquivocadaException, NoSuchAlgorithmException, InvalidKeySpecException, OpcionNoSeleccionadaException, LongitudesMaximasException{
        
        //---------------------------------------------------------------------------------------------------------------------------------------
        //VALIDACIONES:
        if (!Administrador.isSelected() && !Bedel.isSelected()) throw new OpcionNoSeleccionadaException();
        //Campos vacios:
        if("".equals(IdUsuario.getText()) || "".equals(CampoContrasena.getText())) throw new CamposObligatoriosVaciosException();
        if (IdUsuario.getText().length()>50 || CampoContrasena.getText().length()>50) throw new LongitudesMaximasException();
        
        //---------------------------------------------------------------------------------------------------------------------------------------
        boolean adminObedel;
        //admin es true, bedel es false
        if (Administrador.isSelected()){
            adminObedel=true;
            //Validar inicio de sesion:
            GestorContraseña.getInstance().login(IdUsuario.getText(),CampoContrasena.getText(),adminObedel);
            //ir al menu admin
            this.setVisible(false);
            this.dispose();

            // Abrir el nuevo menú
            MenuAdmin nuevoMenu = new MenuAdmin();
            nuevoMenu.setVisible(true);
        } else if (Bedel.isSelected()){
            adminObedel=false;
            //Validar inicio de sesion:
            GestorContraseña.getInstance().login(IdUsuario.getText(),CampoContrasena.getText(),adminObedel);
            //ir al menu bedel
            this.setVisible(false);
            this.dispose();
            // Abrir el nuevo menú
            MenuBedel nuevoMenu = new MenuBedel(usuario);
            nuevoMenu.setVisible(true);
        } 
        
    }
    
