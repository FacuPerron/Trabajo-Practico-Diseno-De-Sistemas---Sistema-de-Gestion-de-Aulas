    private void verPWActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_verPWActionPerformed
        if (evt.getSource() == verPW) {
            if (verPW.isSelected()) {
                // Si está seleccionado, mostrar la contraseña
                CampoContrasena.setEchoChar((char) 0);
            } else {
                // Si no está seleccionado, ocultar la contraseña
                CampoContrasena.setEchoChar('•');
            }
        }
    }//GEN-LAST:event_verPWActionPerformed

    private void SalirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SalirActionPerformed
        // Mostrar cuadro de confirmación
        int opcion = JOptionPane.showConfirmDialog(
            this,
            "¿Estás seguro de que deseas salir?",
            "Confirmar salida",
            JOptionPane.YES_NO_OPTION,
            JOptionPane.WARNING_MESSAGE
        );

        // Si el usuario confirma, cerrar el programa
        if (opcion == JOptionPane.YES_OPTION) {
            this.dispose(); // Cierra la ventana
            System.exit(0);  // Finaliza el programa
        }
    }//GEN-LAST:event_SalirActionPerformed

