    private void llenarTabla(){
        DefaultTableModel modelo = new DefaultTableModel(new Object[][] {}, new String[] {
            "Autor De La Reserva", "Catedra-Seminario-Curso", "CorreoElectronico", "Inicio de la Reserva", "Fin de la Reserva"
        }) {
            @Override
            public boolean isCellEditable(int row, int column) {
                return false; // Hace que ninguna celda sea editable
            }
        };
        
        for (ReservaDTO r : reservas) {
            if (!r.isEsporadica()){
                for(PeriodicaItemDTO i: r.getItemsPeriodica()){
                Object[] reserva = new Object[5];
                reserva[0] = r.getNombreapellidodocente();
                reserva[1] = r.getIdCurso();
                reserva[2] = r.getMailDeContacto();
                reserva[3] = i.getHorario();
                reserva[4] = i.getHoraFin();   
                modelo.addRow(reserva);
                }
            }else {
                for(EsporadicaItemDTO i: r.getItemsEsporadica()){
                Object[] reserva = new Object[5];
                reserva[0] = r.getNombreapellidodocente();
                reserva[1] = r.getIdCurso();
                reserva[2] = r.getMailDeContacto();
                reserva[3] = i.getHorario();
                reserva[4] = i.getHoraFin();   
                modelo.addRow(reserva);
                }
            }        
        }    
        TablaResultados.setModel(modelo);
    }

