    private void fijarTabulacionEnCancelar() {
        // Configuramos una política de tabulación que siempre apunte al botón "Cancelar"
        setFocusTraversalPolicy(new FocusTraversalPolicy() {
            @Override
            public Component getComponentAfter(Container container, Component component) {
                return Cerrar; // Siempre regresa al botón "Cancelar"
            }

            @Override
            public Component getComponentBefore(Container container, Component component) {
                return Cerrar; // Siempre regresa al botón "Cancelar"
            }

            @Override
            public Component getDefaultComponent(Container container) {
                return Cerrar; // El botón "Cancelar" tendrá el foco por defecto
            }

            @Override
            public Component getFirstComponent(Container container) {
                return Cerrar; // El primer componente es "Cancelar"
            }

            @Override
            public Component getLastComponent(Container container) {
                return Cerrar; // El último componente también es "Cancelar"
            }
        });
            // Aseguramos que el foco inicial esté en el botón "Cancelar"
        SwingUtilities.invokeLater(() -> Cerrar.requestFocusInWindow());
    }
