    public void modificarTabla(BedelDTO bdto, int filaSeleccionada) {
        DefaultTableModel modelo = (DefaultTableModel) this.TablaResultados.getModel();
        modelo.setValueAt(bdto.getApellido(), filaSeleccionada, 0);
        modelo.setValueAt(bdto.getNombre(), filaSeleccionada, 1);
        modelo.setValueAt(bdto.getTurno().toString(), filaSeleccionada, 2);
        modelo.setValueAt(bdto.getIdUsuario(), filaSeleccionada, 3);
    }
