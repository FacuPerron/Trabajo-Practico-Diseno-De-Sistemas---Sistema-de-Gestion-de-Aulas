    private void EditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_EditarActionPerformed

        int filaSeleccionada = TablaResultados.getSelectedRow(); // Obtener la fila seleccionada
        int opcion;
        if (filaSeleccionada!=-1){ 
            opcion = JOptionPane.showConfirmDialog(
                this,
                "¿Estás seguro de editar el bedel?",
                "Confirmar modificacion",
                JOptionPane.YES_NO_OPTION,
                JOptionPane.WARNING_MESSAGE
            );
            if (opcion == JOptionPane.YES_OPTION) {
                this.setVisible(false);
                new ModificarBedel(listaBedel.get(filaSeleccionada), this, filaSeleccionada).setVisible(true);
            }
        }else {
            JOptionPane.showMessageDialog(this, "No selecciono un bedel", "Error de seleccion", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_EditarActionPerformed

    private void EliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_EliminarActionPerformed
        int filaSeleccionada = TablaResultados.getSelectedRow(); // Obtener la fila seleccionada
        
        if (filaSeleccionada!=-1){ 
            boolean opcion = new ConfirmarEliminacionBedel(this, listaBedel.get(filaSeleccionada)).getOpcion();
            if(opcion){
                DefaultTableModel modelo = (DefaultTableModel) TablaResultados.getModel();
                try {
                    GestorBedel.getInstance().eliminacionLogica(listaBedel.get(filaSeleccionada).getId());
                    // Eliminar la fila del modelo
                    modelo.removeRow(filaSeleccionada);
                    JOptionPane.showMessageDialog(this, "Bedel eliminado exitosamente");
                    TablaResultados.setModel(modelo);
                }catch (BedelNoEncontradoException ex) {
                    Logger.getLogger(BuscarBedel.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
        }else {
            JOptionPane.showMessageDialog(this, "No selecciono un bedel", "Error de seleccion", JOptionPane.ERROR_MESSAGE);
        }        
        
    }//GEN-LAST:event_EliminarActionPerformed

