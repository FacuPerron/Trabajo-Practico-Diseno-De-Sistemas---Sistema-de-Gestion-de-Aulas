    private void configurarTabulacion() {
        // Configuramos una política de tabulación personalizada
        setFocusTraversalPolicy(new FocusTraversalPolicy() {
            @Override
            public Component getComponentAfter(Container container, Component component) {
                if (component.equals(ApellidoBedel)) return TurnosBedel;
                if (component.equals(TurnosBedel)) return Buscar;
                if (component.equals(Buscar)) return Cancelar;
                if (component.equals(Cancelar)) return Editar;
                if (component.equals(Editar)) return Eliminar;
                return ApellidoBedel; // Vuelve al primero después del último
            }

            @Override
            public Component getComponentBefore(Container container, Component component) {
                if (component.equals(Eliminar)) return Editar;
                if (component.equals(Editar)) return Cancelar;
                if (component.equals(Cancelar)) return Buscar;
                if (component.equals(Buscar)) return TurnosBedel;
                if (component.equals(TurnosBedel)) return ApellidoBedel;
                return Eliminar; // Vuelve al último antes del primero
            }

            @Override
            public Component getDefaultComponent(Container container) {
                return ApellidoBedel; // El componente inicial es "ApellidoBedel"
            }

            @Override
            public Component getFirstComponent(Container container) {
                return ApellidoBedel; // Primer componente del ciclo
            }

            @Override
            public Component getLastComponent(Container container) {
                return Eliminar; // Último componente del ciclo
            }
        });

        // Opcional: Aseguramos que el foco inicial esté en "ApellidoBedel"
        SwingUtilities.invokeLater(() -> ApellidoBedel.requestFocusInWindow());
    }
