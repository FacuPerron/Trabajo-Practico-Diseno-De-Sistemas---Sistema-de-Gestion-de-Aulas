    private void BuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BuscarActionPerformed

        try {
            // Limpiar la tabla antes de agregar nuevos datos
            modelo.setRowCount(0);

            // Obtener el turno seleccionado
            String turno = TurnosBedel.getSelectedItem().toString();
            Turno t = null;
            
            if (!turno.equals("<Seleccione>")) t = Turno.valueOf(turno);
            
            if (ApellidoBedel.getText().length() > 50) throw new LongitudesMaximasException();

            // Obtener la lista de BedelDTO desde GestorBedel
            listaBedel = GestorBedel.getInstance().busqueda(ApellidoBedel.getText(),t);

            // Iterar sobre la lista y agregar las filas al modelo
            for (BedelDTO b : listaBedel) {
                Object[] bedel = new Object[4];
                bedel[0] = b.getApellido();
                bedel[1] = b.getNombre();
                bedel[2] = b.getTurno();
                bedel[3] = b.getIdUsuario();
                modelo.addRow(bedel);
            }

            TablaResultados.setModel(modelo);
        } catch (LongitudesMaximasException e) {
            JOptionPane.showMessageDialog(null, "Error: longitud maxima de algun campo excedida", "registro", JOptionPane.ERROR_MESSAGE);
        }



            
    }//GEN-LAST:event_BuscarActionPerformed
    
