    private void GuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_GuardarActionPerformed
        int opcion;
        opcion = JOptionPane.showConfirmDialog(
            this,
            "¿Estás seguro de guardar la reserva?",
            "Confirmar guardado",
            JOptionPane.YES_NO_OPTION,
            JOptionPane.WARNING_MESSAGE
        );
        if (opcion == JOptionPane.YES_OPTION && modelo.getRowCount()!=0) {
            try {
                GestorRegistros.getInstance().crearReserva(rdto);
                JOptionPane.showMessageDialog(null, "Éxito! Reserva registrada", "registro", JOptionPane.INFORMATION_MESSAGE);
                this.dispose();
                ventanaAnterior.setVisible(true);
            } catch (Exception e) {
                
            }
            
        } else if (modelo.getRowCount()==0) JOptionPane.showMessageDialog(null, "Error: no hay reservas para guardar", "registro", JOptionPane.ERROR_MESSAGE);
    }//GEN-LAST:event_GuardarActionPerformed

    private void EliminarReservaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_EliminarReservaActionPerformed
        int filaSeleccionada = TablaResultados.getSelectedRow();
        int opcion;
        if (filaSeleccionada!=-1){ 
            opcion = JOptionPane.showConfirmDialog(
                this,
                "¿Estás seguro de eliminar la reserva?",
                "Confirmar eliminacion",
                JOptionPane.YES_NO_OPTION,
                JOptionPane.WARNING_MESSAGE
            );
            if (opcion == JOptionPane.YES_OPTION) {
                DefaultTableModel modelo = (DefaultTableModel) TablaResultados.getModel();
                modelo.removeRow(filaSeleccionada);
                rdto.getItemsPeriodica().remove(filaSeleccionada);
            }
        }else {
            JOptionPane.showMessageDialog(this, "No selecciono una reserva", "Error de seleccion", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_EliminarReservaActionPerformed

