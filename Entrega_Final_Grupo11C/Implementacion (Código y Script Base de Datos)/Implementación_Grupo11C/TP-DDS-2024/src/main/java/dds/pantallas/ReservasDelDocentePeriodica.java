    private void configurarTabulacion() {
        // Configuramos una política de tabulación personalizada
        setFocusTraversalPolicy(new FocusTraversalPolicy() {
            @Override
            public Component getComponentAfter(Container container, Component component) {
                if (component.equals(NuevaReserva)) return EliminarReserva;
                if (component.equals(EliminarReserva)) return Guardar;
                if (component.equals(Guardar)) return Cancelar;
                return NuevaReserva; // Vuelve al primero después del último
            }

            @Override
            public Component getComponentBefore(Container container, Component component) {
                if (component.equals(Cancelar)) return Guardar;
                if (component.equals(Guardar)) return EliminarReserva;
                if (component.equals(EliminarReserva)) return NuevaReserva;
                return Cancelar; // Vuelve al último antes del primero
            }

            @Override
            public Component getDefaultComponent(Container container) {
                return NuevaReserva; // Componente que recibe el foco al abrir la ventana
            }

            @Override
            public Component getFirstComponent(Container container) {
                return NuevaReserva; // Primer componente del ciclo
            }

            @Override
            public Component getLastComponent(Container container) {
                return Cancelar; // Último componente del ciclo
            }
        });
    }
